package com.java.Part2_Questions;

public class Somme_Of_100First_Numbers {

	public static void main(String[] args) {

		
		int i, num = 100, sum = 0;  
		  
		for(i = 1; i <= num; ++i)  
		{  
		 
		sum = sum + i;  
		}  
		  
		System.out.println("Sum of First 100 Natural Numbers is = " + sum);  
		

	}

}
